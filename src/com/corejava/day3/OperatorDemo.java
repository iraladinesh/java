package com.corejava.day3;

public class OperatorDemo {

	public static void main(String[] args) {
		//Operators
		
		int x=10;
		System.out.println(x++);
		System.out.println(x);
		System.out.println(++x);
		System.out.println(x);
		
		System.out.println(x--);
		System.out.println(x);
		System.out.println(--x);
		System.out.println(x);

	}

}
